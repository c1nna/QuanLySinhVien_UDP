package model;

import java.io.Serializable;

/**
 *
 * @author Admin
 */
public class Student implements Serializable{
    private String id, name, sex, address, des;
    private double gpa;
    private int age;

    public Student() {
    }

    public Student(String id, String name, double gpa, int age, String sex, String address, String des) {
        this.id = id;
        this.name = name;
        this.sex = sex;
        this.address = address;
        this.des = des;
        this.gpa = gpa;
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }

    public double getGpa() {
        return gpa;
    }

    public void setGpa(double gpa) {
        this.gpa = gpa;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
    public Object[] toObject(){
        return new Object[]{
            id, name, gpa, age, sex, address, des
        };
    }
}
